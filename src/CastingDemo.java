public class CastingDemo {
    public static void main(String[] args) {

        int a = 10;
        int b = 3;

        double c = (double)  a/b;
        // casting - when you want to store one type of value into another type
        // we write that type into round bracket in from of value to be stored

        System.out.println("c="+c);
    }
}
